function checkAdmin() {
    onclick = document.getElementById('divReg').style.display = 'none';
    onclick = document.getElementById('divCome').style.display = 'none';
    onclick = document.getElementById('tableUser').style.display = 'none';
    onclick = document.getElementById('checkAdmin').style.display = 'block';
}
